#include <stdio.h>

int	    is_power_of_2(unsigned int n)
{
	while(n > 0)
	{
		if(n % 2 != 0)
		{
			return (0);
		}
		n = n / 2;
	}
	return (1);
}

int main()
{
	printf("%d\n",is_power_of_2(2));
}
